function plotEllipsoid(center, R, c)
% Define the number of points for plotting
n = 50;

% Create a meshgrid of spherical coordinates
[u, v] = meshgrid(linspace(0, 2*pi, n), linspace(0, pi, n));

% Parametric equations for the unit sphere
x = cos(u) .* sin(v);
y = sin(u) .* sin(v);
z = cos(v);

% Scaling based on c
scale_factors = sqrt(c);  % Assuming c provides the size scaling

% Apply rotation and scaling to each point
X = scale_factors * (R(1,1)*x + R(1,2)*y + R(1,3)*z) + center(1);
Y = scale_factors * (R(2,1)*x + R(2,2)*y + R(2,3)*z) + center(2);
Z = scale_factors * (R(3,1)*x + R(3,2)*y + R(3,3)*z) + center(3);

% Plot the ellipsoid
surf(X, Y, Z,'FaceAlpha',0.3);
axis equal;
% xlabel('X');
% ylabel('Y');
% zlabel('Z');
% title('Ellipsoid');
shading interp;
end

