magData = readCalibFile("StaticCalibData.csv");
% number of measured axes
measPerAxis = 3;
% number of sensored read
numSensors = round(size(magData,2)/measPerAxis);

sz = 4;
axLim = 90; % uT

try
    if(test)
        figure(Units="normalized",Position=[0.1 0.3 0.8 0.35])
        for i = 1:numSensors
            subplot(1,4,i)
            idx = (i-1)*measPerAxis+1;
            [A,b,expmfs] = magcal(magData(:,idx:idx+2));
            scatter3(magData(:,idx),magData(:,idx+1),magData(:,idx+2),sz,"r",'filled')
            plotEllipsoid(b, A*A, expmfs^2)
            C = (magData(:,idx:idx+2)-b)*A; % Calibrated data
            scatter3(C(:,1),C(:,2),C(:,3),sz,"b",'filled')
            plotEllipsoid([0 0 0], eye(3), expmfs^2);
            hold off
            view(45,30)
            xlabel("B_x")
            ylabel("B_y")
            zlabel("B_z")
            axis([-1 1 -1 1 -1 1]*axLim)
        end
    end
catch exception
    figure(Units="normalized",Position=[0.1 0.3 0.8 0.35])
    for i = 1:numSensors
        subplot(1,4,i)
        idx = (i-1)*measPerAxis+1;
        [A,b,expmfs] = magcal(magData(:,idx:idx+2));
        disp("Calibration Data for Sensor "+num2str(i))
        disp("A :")
        disp(A)
        disp("b :")
        disp(b)
        disp("eps :")
        disp(expmfs)
        hold on
        scatter3(magData(:,idx),magData(:,idx+1),magData(:,idx+2),sz,"r",'filled')
        plotEllipsoid(b, A*A, expmfs^2)
        C = (magData(:,idx:idx+2)-b)*A; % Calibrated data
        scatter3(C(:,1),C(:,2),C(:,3),sz,"b",'filled')
        plotEllipsoid([0 0 0], eye(3), expmfs^2)
        hold off
        view(45,30)
        xlabel("B_x")
        ylabel("B_y")
        zlabel("B_z")
        axis([-1 1 -1 1 -1 1]*axLim)
    end
end

